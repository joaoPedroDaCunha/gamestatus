package model.entities;

public class PlayerPerfil {
	
	private String name;
	
	

	public PlayerPerfil() {
		super();
	}

	public PlayerPerfil(String name) {
		super();
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	

}
